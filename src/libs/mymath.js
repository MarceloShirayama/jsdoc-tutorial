/**
 * My math library
 * @module mymath
 */

/**
 * Add two numbers
 * @param {number} x First number
 * @param {number} y Second number
 * @returns {number} Sum: x + y
 */
const add = (x, y) => x + y;

/**
 * Subtract two numbers
 * @param {number} x First number
 * @param {number} y Second number
 * @returns {number} Subtract: x - y
 */
const sub = (x, y) => x - y;

module.exports = { add, sub };
